    protected function getActions(): array
    {
        return [
            LocaleSwitcher::make(),
            Actions\CreateAction::make(),
            Actions\Action::make(__('actions.import'))
                ->icon('heroicon-o-cloud-upload')
                ->form([
                    FileUpload::make('file')
                        ->label(__('actions.import'))
                        ->directory('imports')
                        ->required(),

                    TextInput::make('password')
                        ->label(__('actions.password'))
                        ->password()
                        ->hint(__('actions.import_password_hint'))
                ])

                ->action(function (array $data) {
                    $import = !empty($data['password']) ? new TourguideImport($data['password']) : new TourguideImport();
                    Excel::import($import, "storage/{$data['file']}");
                    Notification::make()
                        ->title(__('attributes.tourguides_imported'))
                        ->success()
                        ->body(__('messages.tourguides_imported_successfully'))
                        ->send();
                }),
        ];
    }

